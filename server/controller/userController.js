export const updateProfile = async (req, res) => {
  const userId = req.id;
  const { name } = req.body;
  const profilePhoto = req.file;

  try {
    const user = await User.findById(userId);
    if (!user) {
      return res.status(404).json({
        message: "User not found",
        success: false,
      });
    }

    let updated = { name };

    if (profilePhoto) {
      if (user.photoUrl) {
        const publicId = user.photoUrl.split("/").pop().split(".")[0];
        deleteMediaCloudinary(publicId);
      }
      const cloudeResponse = await uploadMedia(profilePhoto.path);
      updated.photoUrl = cloudeResponse.secure_url;
      fs.unlinkSync(profilePhoto.path);
    }

    const updatedUser = await User.findByIdAndUpdate(userId, updated, {
      new: true,
    }).select("-password");
    return res.status(200).json({
      success: true,
      user: updatedUser,
      message: "Profile updated successfully.",
    });
  } catch (error) {
    console.log(error);
    return res.status(500).json({
      success: false,
      message: "Failed to update profile",
    });
  }
};